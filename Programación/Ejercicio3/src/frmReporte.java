       DefaultTableModel model=new DefaultTableModel(null,columna);  
       if(lista.size()>0){
           for (Postulante obj:lista){
               if(obj.getCargo().equals(cboCargo.getSelectedItem().toString())){
                   Vector fila=new Vector();
            fila.add(obj.getDni());
            fila.add(obj.getNombres());
            fila.add(obj.getEdad());
            model.addRow(fila);
               }
           }
       }
